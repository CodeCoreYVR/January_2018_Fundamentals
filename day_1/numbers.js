// we call the % operator the modulus or modulo operator
// it gives you the remainder of dividing one number by another

10 % 3 // this will give 1
10 % 5 // this will give 0

// we know that a number is perfectly divisible by another number when the
// the result of % operator is 0

// we can use the `Math` library to do more sophisticated numbers operations

Math.round()
Math.floor()
Math.ceil()
Math.random()

// Write a piece of code that generates a random number between 0 and 99
Math.ceil(Math.random() * 100 - 1)
Math.floor(Math.random() * 100)
Math.round(Math.random() * 99)
